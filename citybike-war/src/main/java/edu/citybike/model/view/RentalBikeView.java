package edu.citybike.model.view;

public class RentalBikeView {

	private String name;
	private String lastName;
	private String userKey;
	private String bikeKey;
	private String rentalOfficeKey;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getUserKey() {
		return userKey;
	}
	public void setUserKey(String userKey) {
		this.userKey = userKey;
	}
	public String getBikeKey() {
		return bikeKey;
	}
	public void setBikeKey(String bikeKey) {
		this.bikeKey = bikeKey;
	}
	public String getRentalOfficeKey() {
		return rentalOfficeKey;
	}
	public void setRentalOfficeKey(String rentalOfficeKey) {
		this.rentalOfficeKey = rentalOfficeKey;
	}
	
	
}
